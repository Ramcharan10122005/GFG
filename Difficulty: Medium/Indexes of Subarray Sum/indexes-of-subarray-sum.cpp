class Solution {
  public:
    vector<int> subarraySum(vector<int> &arr, int target) {
        int l = 0, r = 0, currentSum = 0;
        while (r < arr.size()) {
            currentSum += arr[r];
            while (currentSum > target && l <= r) {
                currentSum -= arr[l];
                l++;
            }
            if (currentSum == target) {
                return {l+1, r+1};
            }
            r++;
        }

        return {-1}; 
    }
};


class Solution {
  public:
    int pairsum(vector<int> &arr) {
        // code here
        int max1=INT_MIN,max2=INT_MIN;
        for(auto n:arr){
            if(n>max1&&n>max2){
                max2=max1;
                max1=n;
            }
            else if(n>max2){
                max2=n;
            }
        }
        return max1+max2;
    }
};


